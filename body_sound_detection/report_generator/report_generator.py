def create_docx_report(data, patient_info=None):
    """
    Create a DOCX report from bowel sound analysis data.
    """
    if patient_info is None:
        patient_info = {"name": "Demo Patient", "age": 45, "gender": "Male"}

    patient_name = patient_info.get("name", "Unknown")
    patient_age = patient_info.get("age", "Unknown")
    patient_gender = patient_info.get("gender", "Unknown")

    classification = data.get("classification", "Unknown")
    duration = data.get("duration_s", 0)
    events = data.get("events_detected", 0)
    rate = data.get("event_rate_per_min", 0)
    snr = data.get("SNR_dB", 0)

    metrics = data.get("event_metrics", {})
    energy = data.get("energy", {})
    band_energy = data.get("150_1000Hz_band", {})

    doc = Document()

    # Title
    title = doc.add_heading('Bowel Sound Analysis Report', 0)
    title.alignment = WD_PARAGRAPH_ALIGNMENT.CENTER

    # Patient Info
    p = doc.add_paragraph()
    p.add_run(
        f"Patient: {patient_name}   |   Age: {patient_age}   |   Gender: {patient_gender}"
    ).bold = True
    doc.add_paragraph(f"Date: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    doc.add_paragraph("---")

    # Summary Table
    summary_table = doc.add_table(rows=4, cols=2)
    summary_table.style = 'LightShading-Accent1'
    summary_table.cell(0, 0).text = "Recording Duration (s)"
    summary_table.cell(0, 1).text = f"{duration:.2f}"
    summary_table.cell(1, 0).text = "Events Detected"
    summary_table.cell(1, 1).text = f"{events}"
    summary_table.cell(2, 0).text = "Event Rate (per min)"
    summary_table.cell(2, 1).text = f"{rate:.1f}"
    summary_table.cell(3, 0).text = "Signal-to-Noise Ratio (SNR dB)"
    summary_table.cell(3, 1).text = f"{snr:.2f}"

    doc.add_paragraph("---")

    # Classification
    doc.add_heading("Classification Result", level=1)
    run = doc.add_paragraph().add_run(f"{classification}")
    run.bold = True

    if classification.lower() in ["abnormal", "low activity", "no activity"]:
        run.font.color.rgb = RGBColor(255, 0, 0)

    # Event Metrics Table
    doc.add_heading("Event Metrics", level=2)
    metrics_table = doc.add_table(rows=3, cols=2)
    metrics_table.style = 'LightShading-Accent2'
    metrics_table.cell(0, 0).text = "Mean Interval (s)"
    metrics_table.cell(0, 1).text = f"{metrics.get('mean_interval_s', 0):.2f}"
    metrics_table.cell(1, 0).text = "Std Interval (s)"
    metrics_table.cell(1, 1).text = f"{metrics.get('std_interval_s', 0):.2f}"
    metrics_table.cell(2, 0).text = "Event Rate (per min)"
    metrics_table.cell(2, 1).text = f"{metrics.get('rate_per_min', 0):.2f}"

    # Spectral Analysis Table
    doc.add_heading("Spectral / Frequency Analysis", level=2)
    spectral_table = doc.add_table(rows=3, cols=2)
    spectral_table.style = 'LightShading-Accent3'
    spectral_table.cell(0, 0).text = "Energy <300 Hz (%)"
    spectral_table.cell(0, 1).text = f"{energy.get('pct_energy_below_300Hz', 0):.2f}"
    spectral_table.cell(1, 0).text = "150â€“1000 Hz Band Energy (%)"
    spectral_table.cell(1, 1).text = f"{band_energy.get('band_energy_pct', 0):.2f}"
    spectral_table.cell(2, 0).text = "Spectral Centroid (Hz)"
    spectral_table.cell(2, 1).text = f"{energy.get('spectral_centroid_hz', 0):.2f}"

    # Impression
    doc.add_heading("Impression", level=2)
    if classification.lower() in ["normal", "active"]:
        doc.add_paragraph("Normal bowel activity detected.")
    elif classification.lower() in ["low activity", "no activity"]:
        doc.add_paragraph("Reduced bowel activity detected. Clinical correlation recommended.")
    elif classification.lower() == "abnormal":
        doc.add_paragraph("Abnormal bowel sound pattern detected. Recommend clinical evaluation.")
    else:
        doc.add_paragraph("Signal quality insufficient or inconclusive result.")

    doc.add_paragraph("---")
    doc.add_paragraph(
        "Disclaimer: This report is generated by AI analysis for demo purposes only. Not intended for clinical diagnosis."
    )

    # Save to memory
    file_stream = BytesIO()
    doc.save(file_stream)
    file_stream.seek(0)
    return file_stream


